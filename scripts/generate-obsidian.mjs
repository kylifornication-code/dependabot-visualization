#!/usr/bin/env node
/**
 * Dependabot Dashboard — Obsidian Note Generator
 *
 * Reads docs/data/current.json and writes Obsidian-compatible markdown notes
 * into a configured vault. Creates:
 *   {vault}/Security/Dependabot/Dashboard.md    — summary + all repos
 *   {vault}/Security/Dependabot/{repo-name}.md  — per-repo detail
 *
 * Vault path (in priority order):
 *   1. OBSIDIAN_VAULT env var
 *   2. Default: ~/Documents/KJ Smoketower
 */

import { writeFileSync, mkdirSync, readFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";
import { homedir } from "os";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");
const DATA_PATH = join(ROOT, "docs", "data", "current.json");

const VAULT =
  process.env.OBSIDIAN_VAULT ||
  join(homedir(), "Documents", "KJ Smoketower");
const OUTPUT_DIR = join(VAULT, "Security", "Dependabot");

// ── Helpers ────────────────────────────────────────────────────────────────

function sev(counts) {
  const { critical = 0, high = 0, medium = 0, low = 0 } = counts;
  const parts = [];
  if (critical) parts.push(`🔴 ${critical} critical`);
  if (high) parts.push(`🟠 ${high} high`);
  if (medium) parts.push(`🟡 ${medium} medium`);
  if (low) parts.push(`🟢 ${low} low`);
  return parts.length ? parts.join(" · ") : "✅ none";
}

function sevBadge(label, count, emoji) {
  return count > 0 ? `${emoji} **${count}** ${label}` : `${emoji} 0 ${label}`;
}

function formatDate(iso) {
  return new Date(iso).toLocaleString("en-US", {
    month: "short", day: "numeric", year: "numeric",
    hour: "2-digit", minute: "2-digit",
  });
}

function prAge(iso) {
  const days = Math.floor((Date.now() - new Date(iso).getTime()) / 86400000);
  if (days === 0) return "today";
  if (days === 1) return "1 day ago";
  if (days < 30) return `${days} days ago`;
  return `${Math.floor(days / 30)}mo ago`;
}

function slug(repoName) {
  return repoName.replace(/\//g, " — ");
}

// ── Dashboard note ─────────────────────────────────────────────────────────

function buildDashboard(data) {
  const { summary, repos, generatedAt } = data;
  const { totalAlerts, bySeverity, totalOpenPRs, reposAffected, reposScanned } = summary;

  const repoRows = repos
    .sort((a, b) => b.totalAlerts - a.totalAlerts)
    .map((r) => {
      const name = r.name;
      const link = `[[${slug(r.repo)}]]`;
      return `| ${link} | ${r.alertCounts.critical ?? 0} | ${r.alertCounts.high ?? 0} | ${r.alertCounts.medium ?? 0} | ${r.alertCounts.low ?? 0} | ${r.totalAlerts} | ${r.openPRs} |`;
    })
    .join("\n");

  return `---
generated: "${generatedAt}"
total_alerts: ${totalAlerts}
critical: ${bySeverity.critical}
high: ${bySeverity.high}
medium: ${bySeverity.medium}
low: ${bySeverity.low}
open_prs: ${totalOpenPRs}
repos_affected: ${reposAffected}
repos_scanned: ${reposScanned}
tags: [security, dependabot, dashboard]
---

# Dependabot Dashboard

> Last updated: **${formatDate(generatedAt)}**

## Summary

| Metric | Value |
|---|---|
| Total open alerts | **${totalAlerts}** |
| ${sevBadge("critical", bySeverity.critical, "🔴")} | |
| ${sevBadge("high", bySeverity.high, "🟠")} | |
| ${sevBadge("medium", bySeverity.medium, "🟡")} | |
| ${sevBadge("low", bySeverity.low, "🟢")} | |
| Open Dependabot PRs | **${totalOpenPRs}** |
| Repos affected | ${reposAffected} / ${reposScanned} scanned |

## Repos

| Repo | 🔴 Crit | 🟠 High | 🟡 Med | 🟢 Low | Total | Open PRs |
|---|---|---|---|---|---|---|
${repoRows}

---

*Generated by [dependabot-visualization](https://github.com/kylifornication-code/dependabot-visualization) · Run \`node scripts/generate-obsidian.mjs\` to refresh*
`;
}

// ── Per-repo note ──────────────────────────────────────────────────────────

function buildRepoNote(r, generatedAt) {
  const prRows = (r.prs || [])
    .map((pr) => {
      const age = prAge(pr.createdAt);
      const compat = pr.compatibilityDisplay ?? "—";
      const labels = (pr.labels || []).join(", ") || "—";
      return `| [#${pr.number}](${pr.url}) | ${pr.title} | ${age} | ${compat} | ${labels} |`;
    })
    .join("\n");

  const prSection =
    r.prs && r.prs.length > 0
      ? `## Open PRs (${r.openPRs})\n\n| # | Title | Age | Compat | Labels |\n|---|---|---|---|---|\n${prRows}`
      : `## Open PRs\n\n_No open Dependabot PRs._`;

  return `---
repo: "${r.repo}"
language: "${r.language || "unknown"}"
total_alerts: ${r.totalAlerts}
critical: ${r.alertCounts.critical ?? 0}
high: ${r.alertCounts.high ?? 0}
medium: ${r.alertCounts.medium ?? 0}
low: ${r.alertCounts.low ?? 0}
open_prs: ${r.openPRs}
generated: "${generatedAt}"
tags: [security, dependabot, repo]
---

# ${r.name}

[[Dashboard]] · [View on GitHub](${`https://github.com/${r.repo}`})

## Alerts (${r.totalAlerts} open)

${sev(r.alertCounts)}

| Severity | Count |
|---|---|
| 🔴 Critical | ${r.alertCounts.critical ?? 0} |
| 🟠 High | ${r.alertCounts.high ?? 0} |
| 🟡 Medium | ${r.alertCounts.medium ?? 0} |
| 🟢 Low | ${r.alertCounts.low ?? 0} |

${prSection}

---

*Last updated: ${formatDate(generatedAt)}*
`;
}

// ── Main ───────────────────────────────────────────────────────────────────

const raw = readFileSync(DATA_PATH, "utf8");
const data = JSON.parse(raw);

mkdirSync(OUTPUT_DIR, { recursive: true });

// Dashboard
writeFileSync(join(OUTPUT_DIR, "Dashboard.md"), buildDashboard(data), "utf8");
console.log(`✓ Dashboard.md`);

// Per-repo notes
for (const repo of data.repos) {
  const filename = `${slug(repo.repo)}.md`;
  writeFileSync(join(OUTPUT_DIR, filename), buildRepoNote(repo, data.generatedAt), "utf8");
  console.log(`✓ ${filename}`);
}

console.log(`\nWrote ${data.repos.length + 1} notes to:\n  ${OUTPUT_DIR}`);
